#!/usr/bin/env bash
# Macos setup (awesome reference: https://raw.githubusercontent.com/mathiasbynens/dotfiles/master/.macos)

# search how to

# keyboard
# add input sources to menu bar
# add spanish iso to sources (and sets as default)
# shortcut
